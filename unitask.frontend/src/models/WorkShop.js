class WorkShop {
    constructor(workshopId, name, description, startDate, endDate, location, regUrl, status) {
        this.workshopId = workshopId;
        this.name = name;
        this.description = description;
        this.startDate = startDate;
        this.endDate = endDate;
        this.location = location;
        this.regUrl = regUrl;
        this.status = status;
    }
}

export default WorkShop;